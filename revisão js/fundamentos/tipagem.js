// JS é uma linguagem fracamente tipada, ou seja, não declara-se o tipo ao criar uma estrutura.
// Essa tipagem ocorre dinamicamente.
let qualquer = "Legal"; // Definindo um texto
console.log(qualquer);
console.log(typeof qualquer); // String

qualquer = 3.1416 // Definindo um número
console.log(qualquer);
console.log(typeof qualquer); // Number